count_start = 0

# While Loop. Syntax- while condition:
print("\n1. While Loop. Syntax- while <condition>:")
while count_start < 10:
    print(count_start)
    count_start = count_start + 1

# For loop. for <variable> in range(start_count, end_count)
print("\n2. For loop. for <variable> in range(start_count, end_count)")
start_count = 0
end_count = 10
for count_start in range(start_count, end_count):
    print(count_start)